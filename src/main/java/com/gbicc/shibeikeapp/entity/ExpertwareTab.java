package com.gbicc.shibeikeapp.entity;


public class ExpertwareTab {
	private String expertwareId;
	
	private String expertwareTitle;
	
	private String expertwareUrl;
	
	private String id;

	public String getExpertwareId() {
		return expertwareId;
	}

	public void setExpertwareId(String expertwareId) {
		this.expertwareId = expertwareId;
	}

	public String getExpertwareTitle() {
		return expertwareTitle;
	}

	public void setExpertwareTitle(String expertwareTitle) {
		this.expertwareTitle = expertwareTitle;
	}

	public String getExpertwareUrl() {
		return expertwareUrl;
	}

	public void setExpertwareUrl(String expertwareUrl) {
		this.expertwareUrl = expertwareUrl;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}
